                    <h3>1. Online Nonimmigrant Visa Application</h3>
                    <p>
                        Online Nonimmigrant Visa Application form, is for temporary travel to the Islamic Republic of Iran. The Application is submitted electronically to E-visa Department, Ministry of Foreign Affairs website via the Internet. Consular Officers use the information entered on the application to process the visa application and, may be combined with a personal interview, determine an applicant's eligibility for a nonimmigrant visa. <br>
                        Visa applicants must submit a completed Online Nonimmigrant Visa Application for all nonimmigrant visa categories, including applicants applying for Entry, Tourist, Work, Treatment, Press, Student, Family, and Investment visas. More information about types of Iranian visas can be find at the following website: <a href="https://e_visa.mfa.ir/en/">www.e_visa.mfa.ir</a><br>
                    </p>
                    <h3>Important Notice</h3>
                    <p>
                        Take care to answer all questions on the electronic visa application form accurately and completely; otherwise, you may have to reapply your application and reschedule your visa interview appointment. <br>
                        Electronically submitting your online application is the FIRST STEP in the visa application process. Some visa applicants will need to attend a visa interview, though some applicants may qualify for a visa without interview. You should wait until your application pass all required process within 10 business days. If the embassy or consulate at which you apply informs that you must have a visa interview, the visa application process cannot be completed until you appear for an interview with a consular officer. By changing your application status, a Status email will be send to your email address.<br>
                    </p>
                    <h6>After you have completed online visa application, you must take these next steps below: </h6>
                    <p>
                        •Print and keep the E-visa application submission notice. This page will be appearing when you completed the email verification process after submitting your online visa application. (You will not need to print the full application.) <br>
