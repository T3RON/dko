                        • You may asked to attend a visa interview based on the appointment system will schedule for you.<br>
                        • If you are not required to attend an interview, you should wait your application pass all required process within 10 business days. By changing your application status, a Status email will be send to your email address.<br>
                    </p>
                    <h6>What You Should Know before Applying</h6>
                    <p>
                        If you want to apply for a visa, please read the following points carefully to avoid any probable case of denial.<br>
                    </p>
                    <h6>Passport Expiration </h6>
                    <p>
                        To apply for all types of electronic visas, your passport should have at least six months of validity, beyond your actual travel date. Obviously, you are not able to submit your application, if your passport is expiring in less than 6 months.<br>
                    </p>
                    <h6>Visa Validity </h6>
                    <p>
                        The Iranian visa sticker displays some information including the validity of visa. Failure to travel to Iran during the validity of visa will prevent you from entering Iran and you will need to re-apply for a visa.<br>
                    </p>
                    <h6>Visa Types</h6>
                    <p>
                        All types of visa defined by the government of the Islamic Republic of Iran can be applied for through the online visa application portal. Notwithstanding, some types of visa for the citizens of some countries should be applied for only in person through the relevant Embassy of the Islamic Republic of Iran.<br>
                    </p>
                    <h6>Visa Eligibility </h6>
                    <p>
                        The citizens of some countries may not be issued visa to enter the Islamic Republic of Iran due to political and diplomatic considerations. Citizens of such countries shall refer to the relevant Embassy of the Islamic Republic of Iran for further information.<br>
                    </p>
                    <h6>Excluded Citizenship </h6>
                    <p>
                        Based on mutual agreement within the framework of international rules and regulations, citizens of some countries are exempt from applying for a visa to enter Iran. They include the citizens of the Republic of Azerbaijan, Bolivia, Turkey, Syria, Lebanon, Georgia, Armenia, Venezuela, Egypt and Malaysia.<br>
                    </p>
                    <h3>Visa on Arrival (Apply for all Iranian International Airports) </h3>
                    <p>
                        It is notified to visitors to the Islamic Republic of Iran intending to acquire their visa at Iranian International Airports that in order to expedite the issuance of visa take the following measures (at least two days prior to their travel): <br>
                        • Apply for an electronic visa application by clicking on the following website: <a href="https://e_visa.mfa.ir/en/">www.e_visa.mfa.ir</a> <br>
                        • You must submit a hardcopy of Grant Notice to the Visa-On-Arrival counter at the airport. The notice will be available when your application for Visa on Arrival is approved and you receive the grant email. <br>
                        • Citizens of foreign countries, who are entitled to obtain visas at border checkpoints, must pay visa fee through banking terminals at airport in euros.<br>
                    </p>
                    <h3>2. Iran Airport Visa (Getting Visa at Airport)</h3>
                    <h6>Get your visa when you arrive at Iran Airport</h6>
                    <p>
                        Obtain Iranian Visa by Arriving "Upon Arrival Visa" at Selected Airports of Iran:
                        Tehran, Mashhad, Shiraz, Isfahan, Kerman, Orumieh, Tabriz, Bushehr, Band Abbas, Kish and Qeshm are available for about seventy nationalities. This method is very convenient. Citizens of the seventy countries holding passports can apply for a 15 - 30 day tourist visa at the airports listed above. Many people prefer to get a visa because of the inconvenience of going to the embassy or consulate. But to get an Iranian visa on arrival, it is always best to get a license number beforehand. You can ask travel companies in Iran to get a license number for you and once the license code is received, the process will be easier and faster. Please note that Iranian visas, like all countries, require valid passports for at least 6 months from the moment of departure.
                    </p>
