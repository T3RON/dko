                    <h3>Women Dress</h3>
                    <p>
                        Perhaps the most visible mark of Iran's Islamic leanings is the conservative dress. Although normal Western-style clothing is acceptable in private homes, when in public women are required to cover their body and hair. Since the revolution of 1979 all women in Iran, including foreigners, have been required by law to wear loose-fitting clothes to disguise their figures. They must also cover their hair. This form of dressing is known as Hejab, a term that refers in general to ‘modest’ dress, and is also used to refer specifically to the hair-covering.<br>
                        Signs in public places show officially acceptable versions of hejab: the chador, an all-encompassing, black garment; or a manteau and a rusari (scarf) covering the hair, neck and décolletage. Girls must start to wear hejab when they reach puberty. As a foreigner, a female traveler is officially expected to cover her hair. Usually more tolerance tends to be shown towards foreigners over the detail of the dress code than is the case for Iranian women. However, this does not include leaving one's hair fully uncovered. "Acceptable" outfits may include a, loose dress or shirt wom over loose skirt or pants and a scarf in the summer, and a woolen coat and scarf in the winter (calf-length is acceptable if worn over pants). All colors and modest designs are acceptable. It's not unusual to see young women in the larger cities wearing figure-hugging manteaus (often tightly belted trench-coats), skinny jeans, high zx heels and colorful rusaris that have been arranged to offer plentiful glimpses. But in the smaller cities, towns and villages this rarely happens - the chador is common and those who don't wear it are clad in an ensemble of shapeless coat, black pants, sensible shoes and a maqna'e (nun-like head scarf, or wimple). Color schemes areuniformly dull.<br>
                        For summer trips to Iran Tonic is a good choice for women.<br>
                    </p>
                    <h3>Head Coverings</h3>
                    <p>
                        You should keep your scarf on in Iran. Silk scarves aren't much use, as they tend to slip off, the only way to make them work is to tie them under the chin babushka-style. Wool can work, but not if it's too fine and slippery. Your best bet is textured cotton, which tends to adhere to hair more effectively and slips less. Make sure that your scarf is wide enough to cover all of your hair, and long enough to be able to throw over your shoulders Some travelers wear a thick elasticized headband and fasten their scarves to it with safety or bobby pins, ensuring that their scarf doesn't slip - this can work well with silk and fine cotton, so is worth considering if you are travelling here over summer and want to wear something light, bring the band with you. Manteaus The majority of manteaus are made from polyester or cheap cotton. The trench-coat style is the most popular version for fashion-conscious Iranian women, but it can be hot and uncomfortable - remember that your manteau will need to stay on in restaurants, cinemas, shops and other interior public spaces. Loosefitting cardigans going down to the mid-thigh are a comfortable, alternative form of outerwear. These can be worn over T-shirts or jumpers (sweaters) but bring them from home - they're hard to source in Iran. In summer, you'll need to wear something light -- long peasant blouses and tunics made with natural fibers work well, as do shalwar kameez, a long shirt or tunic worn over baggy pants. If you're coming overland from India, you'll have plenty of opportunities to purchase these along your journey. All manteaus are worn over trousers; jeans are perfectly acceptable. Do not wear skirts. You can just wear it on your trouser.<br>
                    </p>
                    <h3>Chadors</h3>
                    <p>
                        The only times when foreign women must wear a chador are when visiting important shrines or Imamzadehs. In these instances, the chadors can almost always be borrowed on-site freely.<br>
                    </p>
                    <h3>Will I be Safe? </h3>
                    <p>
                        Violence against foreign women in Iran is almost unheard. Men are also required to abide by the following dress code: Short-sleeved shirts and t-shirts are acceptable for daily wear. Shorts and three-quarter length pants are acceptable on the beach. Dressing for men is similar to that in Europe. It is quite acceptable in the areas outside though it denotes indifference toward or opposition against state regulations and values. Jogging in tracksuits is acceptable for men.<br>
                    </p>
                    <h3>Children</h3>
                    <p>
                        Nappies (diapers), powders, baby formula and most medications are widely available, though not necessarily in familiar brands. Parents should explain fairly clearly to their daughters aged nine or older to wear hijab. Eating with the family is the norm in Iran, and taking your kids into a restaurant will be welcomed. If you have small children and plan on using taxi, you'll probably have to bring your own baby seat. Few vehicles have seatbelts in the back, so you can ask for them when you hire the cab.<br>
