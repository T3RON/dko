                                                                                Glimpse
                                                                                One of the oldest historic monuments in the city of Tehrān, and of world heritage status, the Golestān Palace belongs to a group of royal buildings that were once enclosed within the mud-thatched walls of Tehrān's Arg ("citadel"). It consists of gardens, royal buildings, and collections of Iranian crafts and European presents from the 18th and 19th centuries.
                                                                                History
                                                                                Tehran's Arg (citadel) was built during the reign of Tahmasp I (r. 1524-1576) of the Safavid dynasty (1502-1736), and was later renovated by Karim Khan of the Zand dynasty (r. 1750-1779). Agha Mohammad Khan of the Qajar dynasty (17421797) chose Tehrān as his capital. The Arg became the seat of the Qajars (1794-1925). The court and palace of Golestān became the official residence of the Qajar dynasty. The palace was rebuilt to its current form in 1865 by Haji Abol Hasan Me'mār Navai.
                                                                                During the Pahlavi era (1925-1979), the Golestān Palace was used for formal royal receptions, and the Pahlavi dynasty built their own palace (the Niavaran Complex) in Niavaran. The most important ceremonies held in the palace during the Pahlavi era were the coronation of Reza Shah (r. 19251941) on the Marble Throne and the coronation of Mohammad Reza Pahlavi (r. 1941-deposed 1979) in the Museum Hall.
                                                                                Between 1925 and 1945, a large portion of the buildings of the complex were destroyed on the orders of Reza Shah. He believed that the centuries-old Qajar palace should not hinder the growth of a modern city. In the place of the old buildings, commercial buildings with the modern style of 1950s and 1960s were erected.
                                                                                Edifice of the Sun (Shams al-Emareh)
                                                                                The Edifice of the Sun is considered the most stunning structure of the Golestān Palace. The building has two identical towers. The exterior views have multiple arches, intricate tile work and ornate windows. This building's two towers are in fact small versions of the Safavid viewing palace of Alī Qāpū in Isfahān.
                                                                                Explore in Golestān Palace
                                                                                Karim-Khani Nook (Khalvat-e Karim-Khani)
                                                                                The basic structure of the Karim Khani Nook is similar to the Marble Throne. Like the latter, it is a terrace. There is a small marble throne inside the terrace. The structure is much smaller than the Marble Throne and it has much less ornamentation. There was once a small pond with a fountain in the middle of this terrace. Water from a subterranean stream (the king's Qanāt) flowed from the fountain into the pond and was later used to irrigate the palace grounds.
                                                                                Building of Wind-Catchers (Emarat-e Badgir)
                                                                                The Building of Wind-Catchers was constructed during the reign of Fath Ali Shah. The building underwent major renovations, including structural changes, during the reign of Nasser al-Din Shah.
                                                                                Brilliant Hall (Talar-e Brelian)
                                                                                The Brilliant Hall was named so for it is adorned by the brilliant mirror work of Iranian artisans. The hall was built by the order of Nasser al-Din Shah to replace another hall called Talar-e Bolour (the Crystal Hall). The Brilliant Hall is famous for its mirror work and chandeliers.
                                                                                Pond House (Howz-Khaneh)
                                                                                The Pond House was used as a summer chamber during the Qajar era. A special cooling system pumped water from a subterranean system of streams into small ponds inside the chambers. The system was designed to pass through as many summer rooms as was necessary.

